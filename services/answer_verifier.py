import re

import httpx
from openai import OpenAI

from config import settings


class _CustomHTTPClient(httpx.Client):
    def __init__(self, *args, **kwargs):
        kwargs.pop("proxies", None)
        super().__init__(*args, **kwargs)


def _get_client() -> OpenAI:
    return OpenAI(api_key=settings.OPENAI_API_KEY, http_client=_CustomHTTPClient())


def verify_open_answer(fragment_text: str, question: str, answer: str) -> tuple[str, str]:
    """Grade an open-ended answer. Returns (grade: str, explanation: str)."""
    client = _get_client()

    system = """
        Ты - нейро-экзаменатор, который всегда четко выполняет инструкции. Твоя задача - на основе предоставленного тебе текста проверить правильность ответа студента на вопрос.
        Внимательно проанализируй текст, вопрос, заданный студенту, и ответ студента.
        Оцени правильность ответа студента по шкале от 1 до 5, где:

        Оценка: 1 балл.
        Описание: Ответ полностью неверный, пользователь не владеет информацией, не понял вопрос или описывает совсем другую тему. Верность ответа составляет от 0% до 20%.

        Оценка: 2 балла.
        Описание: Пользователь понял вопрос, но ответ по большей части неверный или выводы логически ошибочны. Верность ответа составляет от 21% до 40%.

        Оценка: 3 балла.
        Описание: Пользователь понял вопрос и отвечал своими словами, но дал не полный ответ на него. Допущены ошибки, и не полностью раскрыта тема вопроса (например, из 5 частей
        ответа 3 верные). Верность ответа составляет от 41% до 60%.

        Оценка: 4 баллов.
        Описание: Пользователь понял вопрос, дал хороший ответ своими словами. Ответ логически верный, но не полностью раскрыта тема или допущена маленькая ошибка.
        Верность ответа составляет от 61% до 80%.

        Оценка: 5 баллов.
        Описание: Пользователь понял вопрос, большая часть ответа верная. Ответ логически верный и тема полностью раскрыта своими словами. Верность ответа составляет от 81% до 100%.

        Ответ студента не обязательно должен слово в слово повторять текст, содержащий ответ. Главное, чтобы по сути он с ним совпадал.
        Учитывай при оценке глубину и точность ответа, а также его соответствие ключевым аспектам вопроса и предоставленного текста.
        Стремись к объективной и точной оценке, основанной на содержании ответа и его релевантности поставленному вопросу.
        Укажи точную оценку и краткий комментарий, почему ты поставил такую оценку в следующем формате:
        1. Оценка ответа по шкале от 0 до 5: "##_ Оценка: "
        2. Краткое пояснение, почему ты поставил такую оценку: "##_ Пояснение: ".
        Строго следуй указанному формату ответа.
    """

    # Few-shot example for better formatting
    user_example = (
        "Оцени правильность ответа студента на вопрос по тексту по шкале от 1 до 5.\n"
        "Текст: 'Теоретический аспект: при исследовании некоторой задачи результаты теории алгоритмов позволяют "
        "ответить на вопрос – является ли эта задача в принципе алгоритмически разрешимой.'\n"
        "Вопрос: 'Какой вопрос задают теоретики алгоритмов при исследовании алгоритмической разрешимости задачи?'\n"
        "Ответ студента: 'Специалисты ставят вопрос о том, относится ли эта задача к классу NP-полных задач.'"
    )
    assistant_example = "##_ Оценка: 5 ##_ Пояснение: ответ студента полностью соответствует заданному вопросу"

    response = client.chat.completions.create(
        model=settings.OPENAI_MODEL,
        temperature=0.1,
        messages=[
            {"role": "system", "content": system},
            {"role": "user", "content": user_example},
            {"role": "assistant", "content": assistant_example},
            {
                "role": "user",
                "content": (
                    f"На основании представленного текста оцени правильность ответа студента на вопрос по шкале от 1 до 5. "
                    f"Учти, что ответ студента необязательно должен слово в слово совпадать с текстом. "
                    f"Главное, чтобы суть была верной. "
                    f"Текст: '{fragment_text}'. Вопрос: '{question}', Ответ студента: '{answer}'"
                ),
            },
        ],
    )

    ans = response.choices[0].message.content

    grade_match = re.search(r"##_ Оценка:\s*(\d+)", ans)
    explanation_match = re.search(r"##_ Пояснение:\s*(.*)", ans, re.DOTALL)

    grade = grade_match.group(1) if grade_match else "Не определена"
    explanation = explanation_match.group(1).strip() if explanation_match else "Пояснение отсутствует"

    return grade, explanation


def verify_test_answers(exam_data: dict) -> None:
    """Grade multiple-choice answers in-place, incrementing exam_data['user_score']."""
    for i in range(len(exam_data["student_answers"])):
        if exam_data["student_answers"][i] == exam_data["correct_answers"][i]:
            exam_data["user_score"] += 1
